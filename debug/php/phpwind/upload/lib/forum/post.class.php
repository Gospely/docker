	function setAtUsers($users = array()){
		if (!$this->_G['allowat']) return false;
		if (is_array($users)) {
			$userService = L::loadClass('UserService', 'user'); 
			$users = $userService->getByUserNames($users);
			$tmpUsers = array();
			foreach ((array) $users as $v) {
				$tmpUsers[$v[uid]] = $v['username'];
			}
		}
		$this->data['contentAtUsers'] && $tmpUsers = (array)$tmpUsers+(array)$this->data['contentAtUsers'];
		($this->_G['atnum'] > 0 && count($tmpUsers) > $this->_G['atnum']) && $tmpUsers = PwArraySlice($tmpUsers, 0 ,$this->_G['atnum'],true);
		$this->data['atusers'] = $tmpUsers;
	}
	
	/**
	 *解析内容中的@用户
	 *@param str $content 
	 */
	function analyseAtByContent($content = null) {
		if (!$this->_G['allowat']) return false;
		if (!$content) $content = $this->data['content'];
		preg_match_all('/@([^\\&\'"\/\*,<>\r\t\n\s#%?@:：]+)\s?/i', $content, $matchs);
		if (!$matchs[1]) return array();
		$users = array();
		$userService = L::loadClass('UserService', 'user');
		$uInfo = $userService->getByUserNames($matchs[1]);
		foreach ($uInfo as $rt) {
			$users[$rt[uid]] = $rt['username'];
		}
		$this->data['contentAtUsers'] = $users;
	}
	
	/**
	 *设置内容中的@用户
	 *@param str $content 
	 */
	function setAtUsersLink($content) {
		$this->analyseAtByContent($content);
		$users = array();
		foreach ((array)$this->data['contentAtUsers'] as $k => $v) {
			$atUsers[] = '@'.$v;
			$users[] = '[url=u.php?uid='.$k.']@'.$v.'[/url]&nbsp;&nbsp;';
		}
		return str_replace($atUsers, $users, $content);
	}
	
